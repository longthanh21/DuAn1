    void loadHoaDon(ArrayList<HoaDon> listHoaDons) {
        defaultTableModel = (DefaultTableModel) tblHoaDon.getModel();
        defaultTableModel.setRowCount(0);
        int stt = 0;
        for (HoaDon hd : listHoaDons) {
            stt++;
            defaultTableModel.addRow(new Object[]{
                stt,
                hd.getMa(),
                hd.getNgay(),
                hd.getTenNV(),
                hd.getTinhTrang()
            });
        }
    }

