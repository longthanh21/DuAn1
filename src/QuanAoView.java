    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        int i = tblSanPham.getSelectedRow();
        int n = tblSanPham.getSelectedColumn();
        String ma = (String) tblSanPham.getValueAt(i, 1);
        String ten = (String) tblSanPham.getValueAt(i, 2);

        String a = JOptionPane.showInputDialog("Nhap so luong");
        String b = (String) tblSanPham.getValueAt(i, 5);
        if (Integer.parseInt(a) < 0) {
            JOptionPane.showMessageDialog(this, "Vui long nhap lai so luong");
            return;
        }
        if (Integer.parseInt(a) > Integer.parseInt(b)) {
            JOptionPane.showMessageDialog(this, "Vui long nhap lai so luong");
            return;
        }
        tblSanPham.setValueAt(Integer.parseInt(b) - Integer.parseInt(a), i, 5);

        String giaBan = (String) tblSanPham.getValueAt(i, 6);
        Double thanhTien = Integer.parseInt(a) * Double.parseDouble(giaBan);

        GioHang gioHang = new GioHang(ma, ten, a, giaBan, thanhTien);

        listGiohang.add(gioHang);
        loadGioHang(listGiohang);

        int tong = 0;
        for (GioHang gh : listGiohang) {
            tong += gh.getThanhTien();

        }
        txtTongTien.setText(tong + "");

    }//GEN-LAST:event_tblSanPhamMouseClicked

